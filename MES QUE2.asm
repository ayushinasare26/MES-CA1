ORG 0000H

MOV A, 50H
MOV 53H, A        

MOV A, 51H
MOV 54H, A       

COMPARE:
MOV A, 53H
JZ A_ZERO         

MOV A, 54H
JZ B_ZERO        

DEC 53H
DEC 54H
SJMP COMPARE


A_ZERO:
MOV A, 54H
JZ EQUAL          
SJMP A_LESS       

B_ZERO:
SJMP A_GREATER    

EQUAL:
MOV 52H, #00H     
SJMP END_PROG

A_GREATER:
MOV 52H, #01H     
SJMP END_PROG

A_LESS:
MOV 52H, #0FFH    

END_PROG:
SJMP END_PROG

END
